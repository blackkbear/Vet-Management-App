    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        // TODO add your handling code here:
         UsuarioDAO dao = new UsuarioDAO();
        Usuario u = new Usuario();

        if(!txtNombreUsuario.getText().equals("") && !txtPassword.getText().equals("")){
            u.setNombreUsuario(txtNombreUsuario.getText());
            u.setPassword(txtPassword.getText());
            u.setTipoUsuario(cboxTipoUsuario.getSelectedItem().toString());

            if(dao.login(u)){
                this.dispose();
                //dispose para ocultar
                String idUsuario = String.valueOf(dao.cargarIdUsuario(txtNombreUsuario.getText()));

                String nomUsuario = txtNombreUsuario.getText();
                String tipoUsuario = cboxTipoUsuario.getSelectedItem().toString();
                frmVentanaPrincipal frm = new frmVentanaPrincipal(idUsuario, nomUsuario, tipoUsuario);
                frm.setVisible(true); // que sea visible
            }
            else{
                JOptionPane.showMessageDialog(null,"Datos Incorrectos");

            }

        }else{
            JOptionPane.showMessageDialog(null,"Debe Ingresar sus datos");
        }

    }//GEN-LAST:event_btnIniciarSesionActionPerformed

